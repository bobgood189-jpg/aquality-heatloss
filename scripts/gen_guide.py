# ── Helpers ───────────────────────────────────────────────────────────────────
def hr():
    return HRFlowable(width='100%', thickness=0.5,
                      color=C_BORDER, spaceAfter=8, spaceBefore=8)

def b(text): return f'<b>{text}</b>'
def i(text): return f'<i>{text}</i>'

def note(text, kind='warn'):
    bg = {'warn': C_WARN_BG, 'tip': C_TIP_BG, 'err': C_ERR_BG}[kind]
    bd = {'warn': C_WARN_BD, 'tip': C_TIP_BD, 'err': C_ERR_BD}[kind]
    icon = {'warn': '!', 'tip': '✓', 'err': '✗'}[kind]
    st = {'warn': ST['warn'], 'tip': ST['tip'], 'err': ST['err']}[kind]
    data = [[Paragraph(f'{icon}  {text}', st)]]
    t = Table(data, colWidths=[PAGE_W - MARGIN * 2 - 4 * mm])
    t.setStyle(TableStyle([
        ('BACKGROUND', (0, 0), (-1, -1), bg),
        ('BOX',        (0, 0), (-1, -1), 0.8, bd),
        ('ROWPADDING', (0, 0), (-1, -1), 6),
        ('TOPPADDING', (0, 0), (-1, -1), 5),
        ('BOTTOMPADDING', (0, 0), (-1, -1), 5),
    ]))
    return t

def step_box(num, title, sub, content_items):
    """Render a numbered step card."""
    header_data = [[
        Paragraph(str(num), ParagraphStyle('SN', fontName='AU-Bold',
                  fontSize=22, textColor=C_AMBER, leading=26)),
        [Paragraph(title, ST['step_h']),
         Paragraph(sub, ST['step_sub'])]
    ]]
    header = Table(header_data,
                   colWidths=[1.2 * cm, PAGE_W - MARGIN * 2 - 1.6 * cm])
    header.setStyle(TableStyle([
        ('VALIGN',      (0, 0), (-1, -1), 'MIDDLE'),
        ('BACKGROUND',  (0, 0), (-1, -1), C_STEP_BG),
        ('BOX',         (0, 0), (-1, -1), 0.8, C_STEP_BD),
        ('TOPPADDING',  (0, 0), (-1, -1), 8),
        ('BOTTOMPADDING',(0,0), (-1, -1), 8),
        ('LEFTPADDING', (0, 0), (0, 0),  10),
        ('RIGHTPADDING',(0, 0), (-1, -1), 8),
    ]))
    items = [header, Spacer(1, 6)]
    items.extend(content_items)
    return KeepTogether(items)

def mat_table(headers, rows):
    col_w = (PAGE_W - MARGIN * 2) / len(headers)
    col_widths = [col_w] * len(headers)
    data = [[Paragraph(b(h), ParagraphStyle('TH', fontName='AU-Bold',
                                             fontSize=9, textColor=white,
                                             leading=12))
             for h in headers]]
    for row in rows:
        data.append([Paragraph(str(c), ParagraphStyle('TC', fontName='AU',
                                                       fontSize=9,
                                                       textColor=C_TEXT,
                                                       leading=13))
                     for c in row])
    t = Table(data, colWidths=col_widths, repeatRows=1)
    t.setStyle(TableStyle([
        ('BACKGROUND',   (0, 0), (-1,  0), C_TABLE_H),
        ('BACKGROUND',   (0, 1), (-1, -1), C_SURFACE),
        ('ROWBACKGROUNDS',(0, 1), (-1, -1), [C_SURFACE, C_BG]),
        ('GRID',         (0, 0), (-1, -1), 0.3, C_BORDER),
        ('TOPPADDING',   (0, 0), (-1, -1), 5),
        ('BOTTOMPADDING',(0, 0), (-1, -1), 5),
        ('LEFTPADDING',  (0, 0), (-1, -1), 6),
        ('RIGHTPADDING', (0, 0), (-1, -1), 6),
        ('VALIGN',       (0, 0), (-1, -1), 'MIDDLE'),
    ]))
    return t

def bullets(items):
    return [Paragraph(f'• {it}', ST['bullet']) for it in items]

def nums(items):
    return [Paragraph(f'{i+1}. {it}', ST['num']) for i, it in enumerate(items)]

