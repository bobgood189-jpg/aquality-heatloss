def on_page(canvas, doc):
    canvas.saveState()
    w, h = A4
    # Header line
    canvas.setStrokeColor(C_BORDER)
    canvas.setLineWidth(0.5)
    canvas.line(MARGIN, h - 1.3 * cm, w - MARGIN, h - 1.3 * cm)
    canvas.setFont('AU', 8)
    canvas.setFillColor(C_MUTED)
    canvas.drawString(MARGIN, h - 1.1 * cm, 'Aquality | WaterPro — Калькулятор теплопотерь')
    canvas.drawRightString(w - MARGIN, h - 1.1 * cm,
                           f'Стр. {doc.page}')
    # Footer
    canvas.line(MARGIN, 1.2 * cm, w - MARGIN, 1.2 * cm)
    canvas.drawCentredString(w / 2, 0.8 * cm,
                              'г. Фергана · aqualityheatloss.netlify.app')
    canvas.restoreState()

